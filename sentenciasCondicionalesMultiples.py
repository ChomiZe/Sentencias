print ("CONVERTIDOR DE NUMEROS EN LETRAS")
print("-----------------------------------")

num_uno = int (input("Cual es número que deseas convertir?: "))
#== (comparacion)

if num_uno == 1:
    print("El número es 'Uno'")
elif num_uno == 2:
    print("El número es 'Dos'")
elif num_uno == 3:
    print("El número es 'Tres'")
elif num_uno == 4:
    print("El número es 'Cuatro'")
elif num_uno == 5:
    print("El número es 'Cinco'")
else:
    print("Este programa solo pude convertir del 1 al 5")
print ("Fin.")
